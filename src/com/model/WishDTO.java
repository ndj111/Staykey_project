package com.model;

public class WishDTO {
    private int wish_no;
    private int wish_stayno;
    private String wish_memid;
    private String wish_date;

    public int getWish_no() {
        return wish_no;
    }
    public void setWish_no(int wish_no) {
        this.wish_no = wish_no;
    }
    public int getWish_stayno() {
        return wish_stayno;
    }
    public void setWish_stayno(int wish_stayno) {
        this.wish_stayno = wish_stayno;
    }
    public String getWish_memid() {
        return wish_memid;
    }
    public void setWish_memid(String wish_memid) {
        this.wish_memid = wish_memid;
    }
    public String getWish_date() {
        return wish_date;
    }
    public void setWish_date(String wish_date) {
        this.wish_date = wish_date;
    }
}
